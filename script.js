let menu = document.querySelector('#menu-icon');
let navlist = document.querySelector('.navlist');

menu.onclick = () => {
    menu.classList.toggle('bx-x');
    navlist.classList.toggle('open');
}

window.onscroll = () => {
    menu.classList.remove('bx-x');
    navlist.classList.remove('open');
}


document.addEventListener('DOMContentLoaded', function () {
    const countdownElement = document.getElementById('timer');
    const targetDate = new Date('2024-06-05T00:00:00').getTime();
  
    function updateCountdown() {
      const now = new Date().getTime();
      const difference = targetDate - now;
  
      const days = Math.floor(difference / (1000 * 60 * 60 * 24));
      const hours = Math.floor((difference % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
      const minutes = Math.floor((difference % (1000 * 60 * 60)) / (1000 * 60));
      const seconds = Math.floor((difference % (1000 * 60)) / 1000);
  
      countdownElement.textContent = `${days.toString().padStart(2, '0')}:${hours.toString().padStart(2, '0')}:${minutes.toString().padStart(2, '0')}:${seconds.toString().padStart(2, '0')}`;
  
      if (difference <= 0) {
        clearInterval(intervalId);
        countdownElement.textContent = 'Countdown Ended';
      }
    }
  
    const intervalId = setInterval(updateCountdown, 1000);
  
    updateCountdown();
});


